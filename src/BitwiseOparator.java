public class BitwiseOparator {
    public static void main(String[] args) {
        int num1=32;
        int num2 =12;
        int c;
        c=num1&num2;
        System.out.println("num1 & num2= "+c);
        c=num1|num2;
        System.out.println("num1 | num2= "+c);
        c=num1^num2;
        System.out.println("num1 ^ num2= "+c);
        c=num1>>2;
        System.out.println("num1 >> 2= "+c);
        c=num1<<2;
        System.out.println("num1 >> 2= "+c);
        c=num1>>2;
        System.out.println("num1 >> 2= "+c);
        c=~num1;
        System.out.println("~num1= "+c);



    }
}
